package com.lh.LeetCode.June.June5;

/**
 * Definition for singly-linked list
 *
 * @Author: LH
 * @Date: 2021/6/5 20:50
 */
public class ListNode {
    int val;
    ListNode next;

    public ListNode() {
    }

    public ListNode(int val) {
        this.val = val;
    }

    public ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}