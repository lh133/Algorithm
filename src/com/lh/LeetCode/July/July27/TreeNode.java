package com.lh.LeetCode.July.July27;

/**
 * Definition for a binary tree node.
 *
 * @Author: LH
 * @Date: 2021/8/2 19:25
 */
public class TreeNode {
    int val;
    TreeNode left;
    TreeNode right;

    TreeNode() {
    }

    TreeNode(int val) {
        this.val = val;
    }

    TreeNode(int val, TreeNode left, TreeNode right) {
        this.val = val;
        this.left = left;
        this.right = right;
    }
}
