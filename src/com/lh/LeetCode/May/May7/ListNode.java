package com.lh.LeetCode.May.May7;

/**
 * Definition for singly-linked list.
 *
 * @Author: LH
 * @Date: 2021/5/7 10:05
 */
public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.next = next;
        this.val = val;
    }
}
