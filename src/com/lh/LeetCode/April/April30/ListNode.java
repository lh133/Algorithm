package com.lh.LeetCode.April.April30;

/**
 * Definition for singly-linked list.
 *
 * @Author: LH
 * @Date: 2021/4/30 14:57
 */
public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
